    private void tblGridMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGridMenuMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() >= 2) {
            MaSP = tblGridMenu.getValueAt(tblGridMenu.getSelectedRow(), 0).toString();
            addSP add = new addSP(this, true);
            add.setVisible(true);

            String tenSP = tblGridMenu.getValueAt(tblGridMenu.getSelectedRow(), 1).toString();
            float thanhTien, dongia;
            dongia = Float.parseFloat(tblGridMenu.getValueAt(tblGridMenu.getSelectedRow(), 2).toString());
            thanhTien = dongia * SoLuong;
            ThemSanPhamTbCTHD(MaSP, tenSP, SoLuong, dongia, thanhTien);
            txtThanhTien.setText(DateHelper.DinhDangTien(TinhTien()));
        }
    }//GEN-LAST:event_tblGridMenuMouseClicked
    private void ThemSanPhamTbCTHD(String maSP, String tenSP, int soLuong,
            float donGia, float thanhTien) {

        DefaultTableModel tbModel = (DefaultTableModel) tblDS.getModel();

        Object obj[] = new Object[6];

        obj[0] = tbModel.getRowCount() + 1;
        obj[1] = maSP;
        obj[2] = tenSP;
        obj[3] = soLuong;
        obj[4] = DateHelper.DinhDangTien(donGia);
        obj[5] = DateHelper.DinhDangTien(thanhTien);

        tbModel.addRow(obj);
    }

    private float TinhTien() {
        float tongTien = 0;
        for (int i = 0; i < tblDS.getRowCount(); i++) {
            tongTien += DateHelper.ChuyenTien(tblDS.getValueAt(i, 5).toString());
        }
        return tongTien;
    }

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        // TODO add your handling code here:
        if (check()) {
            if (tblDS.getRowCount() <= 0) {
                JOptionPane.showMessageDialog(this, "Chưa chọn sản phẩm");
                return;
            }
            NhanVien nv = (NhanVien) cboTenNV.getSelectedItem();
            KhachHang kh = (KhachHang) cboTenKH.getSelectedItem();
            HoaDon model = new HoaDon();
            model.setMaHD(txtMaHD.getText());
            model.setMaNV(nv.getMaNV());
            model.setMaKH(kh.getMaKH());
            model.setThanhTien(DateHelper.ChuyenTien(txtThanhTien.getText()));
            model.setNgayXuatHD(DateHelper.toDate(txtNgayXuat.getText()));
            //Thêm hóa đơn vào database
            hdao.insert(model);
            //Thêm tất cả chi tiết hóa đơn theo MaHD vừa thêm
//            ThemCTHDTuTable(model.getMaHD());
            insert();
            //tạo lại hóa đơn mới
            TaoMoiHD();
            DialogHelper.alert(this, "Đã lưu Hóa đơn!");
        }


    }//GEN-LAST:event_btnThanhToanActionPerformed
