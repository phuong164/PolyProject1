    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGridMenu.getModel();
        model.setRowCount(0);

        try {
            List<Menu> list = dao.select();
            for (Menu mn : list) {
                Object[] row = {
                    mn.getMaSP(),
                    mn.getTenSP(),
                    mn.getGia(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

