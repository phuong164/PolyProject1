    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < tblDS.getRowCount(); i++) {
            String masp = (String) tblDS.getValueAt(i, 0);
            String tensp = (String) tblDS.getValueAt(i, 1);
            int soluong = (int) tblDS.getValueAt(i, 2);
            float gia = (float) tblDS.getValueAt(i, 3);
            Boolean isDelete = (Boolean) tblDS.getValueAt(i, 4);
            if (isDelete) {
                hddao.delete(masp);
            } else {
                HoaDonCT model = new HoaDonCT();
                model.setMaSP(masp);
                model.setTenSP(tensp);
                model.setSoLuong(soluong);
                model.setGia(gia);
                hddao.update(model);
            }
        }
        loadHDCT();
        DialogHelper.alert(this, "Cập nhật thành công!");
    }//GEN-LAST:event_btnCapNhatActionPerformed

