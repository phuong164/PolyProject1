    void load() {
        DefaultTableModel model = (DefaultTableModel) tblGrid.getModel();
        model.setRowCount(0);
        
        try {
            List<Menu> list = dao.select();
            for (Menu mn : list) {
                Object[] row = {
                    mn.getMaSP(),
                    mn.getTenSP(),
                    mn.getGia(),
                    mn.getHinh()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void edit() {
        try {
            String maMenu = (String) tblGrid.getValueAt(this.index, 0);
            Menu model = dao.findById(maMenu);
            if (model != null) {
                this.setModel(model);
                this.setStatus(false);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
